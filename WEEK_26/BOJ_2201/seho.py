k = int(input())

dp = [1, 1]
if k == 1 :
    print(1)
elif k == 2 :
    print(10)
else :
    ans = ""
    i = 2
    sum = 2
    while True:
        dp.append(dp[i-1] + dp[i-2])
        sum += dp[-1]
        i += 1
        if k <= sum:
            break

    for i in range(i-2, -1, -1):
        sum -= dp[i+1]
        if k > sum:
            ans += "1"
            k -= sum+1
        else:
            ans += "0"
    ans += str(k)

    print(ans)
